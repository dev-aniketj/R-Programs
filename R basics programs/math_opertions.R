#max() - use to find the max number.
max(4,2,7,3)  #7

#min() - use to find the min number.
min(4,1,6,2)  #1

#sqrt() - use to get the square root of the number.
sqrt(25)      #5
sqrt(81)

#abs() - use to get the absolute value of a number.
abs(-2.5)     #2.5
abs(-7)       #7
abs(4)

#ceiling() - use to round a number upwards to its nearest integer.
ceiling(3.2)  #4
ceiling(-2.2) #-2

#floor() - use to round a number downwards to its nearest integer.
floor(3.2)    #3
floor(-2.2)   #-2
